    private void jButton57ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton57ActionPerformed
        //STORNARE
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        try {
            int SelectedRowIndex = jTable1.getSelectedRow();
            boolean flag = false;
            for (int i = 0; i < pozitiiStornate.length - 1; i++) {
                if (pozitiiStornate[i] == SelectedRowIndex) {
                    flag = true;
                }
            }

            if (flag == false) {
                pozitiiStornate[aux] = SelectedRowIndex;
                pozitiiStornate[aux + 1] = SelectedRowIndex + 1;
                aux += 2;

                String nume = (String) model.getValueAt(SelectedRowIndex, 0);
                double pret = (Double) model.getValueAt(SelectedRowIndex, 1);
                int nrBuc = (Integer) model.getValueAt(SelectedRowIndex, 2);
                double total = (Double) model.getValueAt(SelectedRowIndex, 3);
                total *= -1;

                //Adaugare Linia Stornata in tabel
                model.insertRow(SelectedRowIndex + 1, new Object[]{});// linie noua in tabel urmand sa fie adaugata stornarea produsului selectat
                model.setValueAt("***STORNARE*** " + nume, SelectedRowIndex + 1, 0);
                model.setValueAt(pret, SelectedRowIndex + 1, 1);
                model.setValueAt(nrBuc, SelectedRowIndex + 1, 2);
                model.setValueAt((Object) total, SelectedRowIndex + 1, 3);
                contorTabelLinii++;
                calculeazaTotal();
            } else {
                JOptionPane.showMessageDialog(null, "Produsul a fost sters deja!",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nu ati selectat produsul pe care doriti sa il sornati!",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton57ActionPerformed


